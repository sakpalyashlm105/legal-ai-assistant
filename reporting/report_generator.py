def render_markdown(report: LegalDocumentReport) -> str:
    """
    Render the LegalDocumentReport to Markdown.

    Section order follows Section 30 of the project's original system prompt:
    executive summary, document metadata, classification, clause-analysis table,
    high-risk findings, medium-risk findings, missing clauses, template deviations,
    source evidence/pages, human-review decisions, recommendations, confidence
    and limitations, processing notes, disclaimer.
    """
    lines: List[str] = []

    lines.append("# Legal Document Analysis Report")
    lines.append("")

    lines.append("## Executive Summary")
    lines.append("")
    lines.append(report.executive_summary)
    lines.append("")

    lines.append("## Document Metadata")
    lines.append("")
    lines.append("| Field | Value |")
    lines.append("|---|---|")
    lines.append(f"| Document Name | {report.document_name} |")
    lines.append(f"| Document Type | {report.document_type} |")
    lines.append(f"| Classification Confidence | {report.classification_confidence_label} |")
    lines.append(f"| Total Pages | {report.total_pages} |")
    lines.append(f"| Clauses Found | {report.total_clauses_found} / 10 |")
    lines.append(f"| Clauses Missing | {report.total_clauses_missing} |")
    lines.append(f"| Document Hash | `{report.document_hash[:16]}...` |")
    lines.append(f"| Generated At | {report.generated_at.strftime('%Y-%m-%d %H:%M UTC')} |")
    lines.append("")

    lines.append("## Clause Analysis")
    lines.append("")
    lines.append("| Clause Category | Present | Confidence | Evidence Verified | Risk Level | Page |")
    lines.append("|---|---|---|---|---|---|")
    for entry in report.clause_entries:
        present = "Yes" if entry.is_present else "**No**"
        ev = ("Yes" if entry.evidence_verified else "No") if entry.evidence_verified is not None else "N/A"
        risk = entry.risk_level or "—"
        page = str(entry.source_page) if entry.source_page else "—"
        lines.append(
            f"| {entry.clause_category} | {present} | {entry.confidence_label} "
            f"| {ev} | {risk} | {page} |"
        )
    lines.append("")

    # Compute rejected categories for exclusion from risk tables
    _rejected = {
        d.get("clause_category") for d in (report.human_review_decisions or [])
        if d.get("action") == "reject" and d.get("discarded", False)
    }

    lines.append("## High-Risk Findings")
    lines.append("")
    high = [r for r in report.risk_findings if r.risk_level == "HIGH" and r.clause_category not in _rejected]
    if high:
        for finding in high:
            lines.append(f"### {finding.clause_category}")
            lines.append("")
            lines.append(finding.finding_summary)
            if finding.source_page:
                lines.append(f"*Source page: {finding.source_page}*")
            if finding.precedent_applied:
                lines.append("*Note: A precedent override was applied to this finding.*")
            lines.append("")
    else:
        lines.append("No high-risk findings were identified.")
        lines.append("")

    lines.append("## Medium-Risk Findings")
    lines.append("")
    medium = [r for r in report.risk_findings if r.risk_level == "MEDIUM" and r.clause_category not in _rejected]
    if medium:
        for finding in medium:
            lines.append(f"### {finding.clause_category}")
            lines.append("")
            lines.append(finding.finding_summary)
            if finding.source_page:
                lines.append(f"*Source page: {finding.source_page}*")
            lines.append("")
    else:
        lines.append("No medium-risk findings were identified.")
        lines.append("")

    lines.append("## Missing Clauses")
    lines.append("")
    if report.missing_clauses:
        for name in report.missing_clauses:
            lines.append(f"- **{name}** — clause was not found in the document.")
        lines.append("")
        lines.append(
            "_Each missing clause has been flagged as a potential risk. "
            "Qualified legal review is recommended to assess whether any omission is material._"
        )
    else:
        lines.append("All 10 standard clause categories were found in this document.")
    lines.append("")

    deviations = [e for e in report.clause_entries if e.deviation_summary]
    lines.append("## Template Deviations")
    lines.append("")
    if deviations:
        for entry in deviations:
            lines.append(f"**{entry.clause_category}** (page {entry.source_page or '—'})")
            lines.append("")
            lines.append(entry.deviation_summary)
            lines.append("")
    else:
        lines.append("No material template deviations were identified.")
    lines.append("")

    lines.append("## Source Evidence and Page References")
    lines.append("")
    evidence_entries = [e for e in report.clause_entries if e.is_present]
    if evidence_entries:
        lines.append("| Clause Category | Page | Evidence Verified |")
        lines.append("|---|---|---|")
        for entry in evidence_entries:
            page = str(entry.source_page) if entry.source_page else "—"
            ev = ("Yes" if entry.evidence_verified else "No") if entry.evidence_verified is not None else "N/A"
            lines.append(f"| {entry.clause_category} | {page} | {ev} |")
    else:
        lines.append("No clauses were present to verify.")
    lines.append("")

    lines.append("## Human Review Decisions")
    lines.append("")
    lines.append(
        "_Findings are separated below by review status. Rejected findings "
        "are excluded from the risk summary tables above._"
    )
    lines.append("")

    decisions = report.human_review_decisions or []
    decided_categories = {d.get("clause_category") for d in decisions if d.get("clause_category")}
    rejected_categories = {
        d.get("clause_category") for d in decisions
        if d.get("action") == "reject" and d.get("discarded", False)
    }
    approved_categories = {d.get("clause_category") for d in decisions if d.get("action") == "approve"}
    corrected_categories = {d.get("clause_category") for d in decisions if d.get("action") == "correct"}

    # AI-only findings: present in risk_findings but not reviewed by a human
    ai_only = [
        r for r in report.risk_findings
        if r.clause_category not in decided_categories
    ]
    # Human-approved findings
    human_approved = [
        r for r in report.risk_findings
        if r.clause_category in approved_categories
    ]
    # Human-corrected findings
    human_corrected = [
        r for r in report.risk_findings
        if r.clause_category in corrected_categories
    ]

    lines.append("### AI Findings (Not Yet Reviewed)")
    lines.append("")
    if ai_only:
        for r in ai_only:
            lines.append(f"- **{r.clause_category}** [{r.risk_level}]: {r.finding_summary}")
    else:
        lines.append("All findings have been reviewed by a human.")
    lines.append("")

    lines.append("### Human-Approved Findings")
    lines.append("")
    if human_approved:
        for r in human_approved:
            d = next((x for x in decisions if x.get("clause_category") == r.clause_category and x.get("action") == "approve"), {})
            note = d.get("reviewer_note") or d.get("reason") or "—"
            lines.append(f"- **{r.clause_category}** [{r.risk_level}]: AI finding confirmed. Reviewer note: {note}")
    else:
        lines.append("No findings were explicitly approved by a human reviewer in this run.")
    lines.append("")

    lines.append("### Human-Corrected Findings")
    lines.append("")
    if human_corrected:
        for r in human_corrected:
            d = next((x for x in decisions if x.get("clause_category") == r.clause_category and x.get("action") == "correct"), {})
            corrected_risk = d.get("corrected_risk_level") or r.risk_level
            corrected_summary = d.get("corrected_summary") or "(see corrected_value in state)"
            note = d.get("reviewer_note") or d.get("reason") or "—"
            lines.append(
                f"- **{r.clause_category}**: AI risk={r.risk_level} → corrected risk={corrected_risk}. "
                f"Correction: {corrected_summary} Reviewer note: {note}"
            )
    else:
        lines.append("No findings were corrected by a human reviewer in this run.")
    lines.append("")

    lines.append("### Rejected Findings (Excluded from Risk Summary)")
    lines.append("")
    lines.append(
        "_These findings were discarded by a human reviewer and do NOT appear "
        "in the High-Risk or Medium-Risk sections above._"
    )
    lines.append("")
    rejected_decisions = [d for d in decisions if d.get("action") == "reject" and d.get("discarded", False)]
    if rejected_decisions:
        for d in rejected_decisions:
            cat = d.get("clause_category", "Unknown")
            note = d.get("reviewer_note") or d.get("reason") or "—"
            lines.append(f"- **{cat}**: Rejected. Reviewer note: {note}")
    else:
        lines.append("No findings were rejected in this run.")
    lines.append("")

    lines.append("## Recommendations")
    lines.append("")
    has_recommendations = False
    for entry in report.clause_entries:
        if entry.recommendation:
            lines.append(f"- {entry.recommendation}")
            has_recommendations = True
    if not has_recommendations:
        lines.append("No specific recommendations were generated.")
    lines.append("")

    lines.append("## Confidence and Limitations")
    lines.append("")
    lines.append("### Confidence Labels")
    lines.append("")
    lines.append(
        "All confidence levels in this report use HIGH / MODERATE / LOW labels. "
        "These labels correspond to the system's internal routing thresholds: "
        "HIGH (>= 0.70) reflects the auto-proceed confidence boundary; "
        "MODERATE (0.50–0.69) reflects the range where additional reasoning was applied; "
        "LOW (< 0.50) reflects cases where the system was uncertain and human review "
        "is most important. These labels are NOT calibrated statistical probabilities."
    )
    lines.append("")
    lines.append("### Limitations")
    lines.append("")
    lines.append(report.limitations_note)
    lines.append("")

    if report.processing_notes:
        lines.append("## Processing Notes")
        lines.append("")
        lines.append(report.processing_notes)
        lines.append("")

    lines.append("## Guardrail Summary")
    lines.append("")
    if report.guardrail_summary:
        lines.append("| Guardrail | Passed | Severity | Notes |")
        lines.append("|---|---|---|---|")
        for g in report.guardrail_summary:
            status = "Yes" if g.passed else "No"
            lines.append(f"| {g.guardrail_name} | {status} | {g.severity} | {g.reason} |")
    else:
        lines.append("No guardrail data recorded.")
    lines.append("")

    lines.append("## Disclaimer")
    lines.append("")
    lines.append(f"_{report.disclaimer}_")
    lines.append("")

    return "\n".join(lines)


