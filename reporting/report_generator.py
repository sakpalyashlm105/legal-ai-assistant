DISCLAIMER = (
    "This report was generated by an AI-assisted legal document analysis system. "
    "It does not constitute legal advice and must not be relied upon as a substitute "
    "for qualified legal counsel. The system identifies patterns and potential areas "
    "of concern; it does not determine whether any clause, contract, or agreement is "
    "enforceable, valid, or fit for any specific purpose. All findings should be "
    "reviewed by a qualified legal professional before any binding decision is made."
)

# ---------------------------------------------------------------------------
# Confidence thresholds (mirroring CLAUDE.md routing boundaries exactly)
# ---------------------------------------------------------------------------

_HIGH_THRESHOLD = 0.70    # >= this -> HIGH (auto-proceed boundary from CLAUDE.md)
_MODERATE_FLOOR = 0.50    # >= this -> MODERATE (ToT routing band from CLAUDE.md)
# < _MODERATE_FLOOR -> LOW


def confidence_to_label(confidence: float) -> tuple[str, str]:
    """
    Translate a 0-1 confidence float to a (label, explanation) tuple.

    Returns
    -------
    label : str
        "HIGH", "MODERATE", or "LOW"
    explanation : str
        One sentence suitable for a human-facing report explaining WHY this
        label applies. Never states a calibrated probability.

    Thresholds:
        >= 0.70 -> HIGH     (same as auto-proceed in classifier/extractor)
        0.50-0.69 -> MODERATE (same as ToT routing band)
        < 0.50  -> LOW      (below retry floor; extraction was uncertain)
    """
    if confidence >= _HIGH_THRESHOLD:
        return (
            "HIGH",
            "High confidence: the system located and verified this clause with "
            "strong signal from the source document.",
        )
    elif confidence >= _MODERATE_FLOOR:
        return (
            "MODERATE",
            "Moderate confidence: the system identified this clause but the "
            "extracted text required additional reasoning to resolve ambiguity.",
        )
    else:
        return (
            "LOW",
            "Low confidence: the system was uncertain about this extraction; "
            "the result should be independently verified by a reviewer.",
        )


