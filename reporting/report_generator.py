def render_json(report: LegalDocumentReport) -> str:
    """
    Serialize the LegalDocumentReport to a JSON string.

    Uses Pydantic's model_dump(mode='json') for correct datetime serialisation.
    Indented at 2 spaces for readability.
    """
    return json.dumps(report.model_dump(mode="json"), indent=2, ensure_ascii=False)
