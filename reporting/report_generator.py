def build_clause_entries(
    clauses: List[ExtractedClause],
    risk_findings: List[RiskFinding],
    evidence_results: Optional[List[Dict]] = None,
) -> List[ClauseReportEntry]:
    """
    Build one ClauseReportEntry per clause.

    Parameters
    ----------
    clauses : list[ExtractedClause]
        All extracted clauses (always 10 per document).
    risk_findings : list[RiskFinding]
        Risk findings, one per clause category.
    evidence_results : list[dict] or None
        Evidence verification result dicts from node_verify_clauses.
        Each dict has at least: clause_type, evidence_found.
        None if the evidence verifier did not run.

    Returns
    -------
    list[ClauseReportEntry]
        One entry per clause, in the order supplied.
    """
    risk_by_type: Dict[str, RiskFinding] = {f.clause_type: f for f in risk_findings}
    evidence_by_type: Dict[str, bool] = {}
    if evidence_results:
        for ev in evidence_results:
            ctype = ev.get("clause_type")
            if ctype is not None:
                evidence_by_type[ctype] = bool(ev.get("evidence_found"))

    entries = []
    for clause in clauses:
        if clause.is_present:
            label, explanation = confidence_to_label(clause.confidence)
        else:
            # Clause is absent — the extractor found no text to evaluate.
            # confidence=0.0 on an absent clause is not a low-confidence
            # extraction; it means confidence is not applicable.  Showing
            # "LOW" here would imply the system tried and was uncertain,
            # which is wrong.
            label = "N/A"
            explanation = "Clause is absent — extraction confidence is not applicable."
        finding = risk_by_type.get(clause.clause_type)
        risk_level = finding.risk_level if finding else None
        deviation = finding.deviation_summary if finding else None

        # Build deviation_summary in observation style if one exists
        deviation_entry: Optional[str] = None
        if deviation:
            deviation_entry = (
                f"The system identified a deviation from the standard template: "
                f"{deviation} Qualified legal review is recommended."
            )

        # Build recommendation
        recommendation: Optional[str] = None
        if not clause.is_present:
            recommendation = (
                f"The {clause.clause_type} clause was not found in this document. "
                "Its absence has been flagged as a potential risk. "
                "Qualified legal review is recommended to determine whether this "
                "omission is intentional or material."
            )
        elif risk_level in ("HIGH", "MEDIUM"):
            recommendation = (
                f"The system identified a {risk_level.lower()}-risk finding for "
                f"the {clause.clause_type} clause. "
                "Qualified legal review is recommended before execution."
            )

        evidence_verified = evidence_by_type.get(clause.clause_type) if clause.is_present else None

        entries.append(ClauseReportEntry(
            clause_category=clause.clause_type,
            is_present=clause.is_present,
            confidence_label=label,
            confidence_explanation=explanation,
            source_page=clause.page_reference,
            evidence_verified=evidence_verified,
            risk_level=risk_level,
            deviation_summary=deviation_entry,
            recommendation=recommendation,
        ))

    return entries


def build_risk_findings(risk_findings: List[RiskFinding]) -> List[RiskFindingEntry]:
    """
    Build one RiskFindingEntry per RiskFinding, sorted HIGH -> MEDIUM -> LOW.

    All finding_summary text follows the observation + recommendation pattern.
    Verdict-style language is never produced here.
    """
    def _sort_key(f: RiskFinding) -> int:
        return {"HIGH": 0, "MEDIUM": 1, "LOW": 2}.get(f.risk_level, 3)

    sorted_findings = sorted(risk_findings, key=_sort_key)

    entries = []
    for f in sorted_findings:
        if f.is_missing:
            summary = (
                f"The system identified that the {f.clause_type} clause is absent "
                f"from this document. Absence of this clause type is classified as "
                f"{f.risk_level} risk. Qualified legal review is recommended to "
                "assess whether this omission is material."
            )
        else:
            base = f.reason or f"The {f.clause_type} clause was assessed at {f.risk_level} risk."
            summary = (
                f"The system identified the following for the {f.clause_type} clause: "
                f"{base} Qualified legal review is recommended."
            )
            if f.precedent_applied and f.precedent_note:
                summary += f" Note: {f.precedent_note}"

        source_page: Optional[int] = None
        if f.source_clause and f.source_clause.page_reference:
            source_page = f.source_clause.page_reference

        entries.append(RiskFindingEntry(
            clause_category=f.clause_type,
            risk_level=f.risk_level,
            finding_summary=summary,
            source_page=source_page,
            precedent_applied=f.precedent_applied,
            human_review_status="not_required",
        ))

    return entries


def build_guardrail_summary(guardrail_results: List[Dict]) -> List[GuardrailSummaryEntry]:
    """
    Build one GuardrailSummaryEntry per guardrail result dict.

    PII-safe: passes through the reason field from GuardrailResult, which
    was already sanitised at guardrail time (no contract text, no injection
    patterns, no personal identifiers).
    """
    entries = []
    for gr in guardrail_results:
        entries.append(GuardrailSummaryEntry(
            guardrail_name=gr.get("guardrail_name", "unknown"),
            passed=bool(gr.get("passed", True)),
            severity=gr.get("severity", "info"),
            reason=gr.get("reason", "No details recorded."),
        ))
    return entries


def assemble_report(
    document_name: str,
    document_hash: str,
    document_type: str,
    classification_confidence: float,
    total_pages: int,
    clauses: List[ExtractedClause],
    risk_findings: List[RiskFinding],
    guardrail_results: List[Dict],
    evidence_results: Optional[List[Dict]] = None,
    human_review_decisions: Optional[List[Dict]] = None,
    executive_summary: str = "",
    processing_notes: Optional[str] = None,
) -> LegalDocumentReport:
    """
    Assemble a complete LegalDocumentReport from pipeline outputs.

    This is the main entry point called by the orchestrator's generate_report
    node. It calls all the build_* helpers and applies the fixed DISCLAIMER.

    Parameters
    ----------
    executive_summary : str
        Pre-generated executive summary text (from executive_summary.py or
        the fallback template). Passed in rather than generated here to keep
        this module free of LLM calls.
    """
    conf_label, _ = confidence_to_label(classification_confidence)

    clause_entries = build_clause_entries(clauses, risk_findings, evidence_results)
    risk_entries = build_risk_findings(risk_findings)
    guardrail_entries = build_guardrail_summary(guardrail_results)

    present = [c for c in clauses if c.is_present]
    missing_names = [c.clause_type for c in clauses if not c.is_present]

    # Limitations note: flag clause categories without template comparison data
    no_deviation_data = [
        e.clause_category for e in clause_entries
        if e.is_present and e.deviation_summary is None
    ]
    if no_deviation_data:
        limitations_note = (
            f"Template comparison data was not available for "
            f"{len(no_deviation_data)} of 10 clause categories "
            f"({', '.join(no_deviation_data[:3])}{'...' if len(no_deviation_data) > 3 else ''}). "
            "Risk assessments for those categories reflect only clause presence/absence, "
            "not deviation from a standard baseline."
        )
    else:
        limitations_note = (
            "Template comparison data was available for all 10 clause categories."
        )

    if not executive_summary:
        executive_summary = _fallback_executive_summary(
            document_type=document_type,
            total_found=len(present),
            risk_entries=risk_entries,
            missing_names=missing_names,
            clause_entries=clause_entries,
        )

    decisions = human_review_decisions or []

    return LegalDocumentReport(
        document_name=document_name,
        document_hash=document_hash,
        document_type=document_type,
        classification_confidence_label=conf_label,
        total_pages=total_pages,
        total_clauses_found=len(present),
        total_clauses_missing=len(missing_names),
        executive_summary=executive_summary,
        clause_entries=clause_entries,
        risk_findings=risk_entries,
        missing_clauses=missing_names,
        guardrail_summary=guardrail_entries,
        human_review_decisions=decisions,
        limitations_note=limitations_note,
        disclaimer=DISCLAIMER,
        generated_at=datetime.utcnow(),
        processing_notes=processing_notes,
    )


def _fallback_executive_summary(
    document_type: str,
    total_found: int,
    risk_entries: List[RiskFindingEntry],
    missing_names: List[str],
    clause_entries: Optional[List] = None,
) -> str:
    """
    Deterministic fallback summary used when the LLM call is unavailable
    or produces a rule-violating response.

    Follows both writing-quality rules: HIGH/MODERATE/LOW labels only,
    observation + recommendation pattern, no verdict language.

    Evidence verification phrasing explicitly distinguishes present-and-verified
    from absent-and-N/A so the summary cannot be read as a blanket reassurance.
    """
    high = [r for r in risk_entries if r.risk_level == "HIGH"]
    medium = [r for r in risk_entries if r.risk_level == "MEDIUM"]

    parts = [
        f"This {document_type} contains {total_found} of 10 standard clause categories."
    ]

    if missing_names:
        parts.append(
            f"The following clause categories were not found: "
            f"{', '.join(missing_names)}."
        )

    if high:
        parts.append(
            f"{len(high)} high-risk finding(s) were identified "
            f"({', '.join(r.clause_category for r in high)})."
        )
    if medium:
        parts.append(
            f"{len(medium)} medium-risk finding(s) were identified "
            f"({', '.join(r.clause_category for r in medium)})."
        )

    # Evidence verification: distinguish verified-and-passed from absent-and-N/A.
    # Never describe absent clauses as "no issues" -- they were never checked.
    if clause_entries is not None:
        ev_passed = sum(1 for e in clause_entries if e.is_present and e.evidence_verified is True)
        na_count = sum(1 for e in clause_entries if not e.is_present)
        if total_found > 0:
            parts.append(
                f"All {ev_passed} present clause(s) passed evidence verification; "
                f"the remaining {na_count} clause(s) were absent and not applicable "
                "for this check."
            )

    parts.append("Qualified legal review is recommended before execution.")
    return " ".join(parts)


