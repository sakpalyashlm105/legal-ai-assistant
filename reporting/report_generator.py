"""
reporting/report_generator.py
------------------------------
Deterministic assembly and formatting of the LegalDocumentReport.

This module contains ZERO LLM calls. Everything here is pure Python:
looping over Pydantic objects, translating floats to labels, formatting
markdown tables, and serializing to JSON. The one LLM call (executive
summary generation) lives in reporting/executive_summary.py (Stage 3)
and is called from the orchestrator node, not from this module.

Why split generation from formatting?
    The assembly functions (build_*) are testable with simple unit tests
    because they have no external dependencies. The markdown/JSON renderers
    are testable by asserting on string content. If the executive summary LLM
    call is separately mocked, the rest of this module is fully deterministic
    and can run without any network calls.

Public API
----------
    confidence_to_label(confidence: float) -> tuple[str, str]
    build_clause_entries(...) -> list[ClauseReportEntry]
    build_risk_findings(risk_findings) -> list[RiskFindingEntry]
    build_guardrail_summary(guardrail_results) -> list[GuardrailSummaryEntry]
    assemble_report(...) -> LegalDocumentReport
    render_markdown(report: LegalDocumentReport) -> str
    render_json(report: LegalDocumentReport) -> str

Confidence label thresholds (justified below):
    >= 0.70 -> HIGH
    0.50-0.69 -> MODERATE
    < 0.50  -> LOW

Threshold justification:
    These mirror the confidence routing boundaries already locked in CLAUDE.md
    and implemented in classifier.py / extractor.py:
      > 0.70 -> auto-proceed (HIGH confidence: system is sure enough to proceed)
      0.50-0.70 -> Tree-of-Thought (MODERATE confidence: needs more reasoning)
      < 0.50 -> retry then HITL (LOW confidence: system is uncertain)
    Using the same boundaries in the report ensures that what the report calls
    "HIGH confidence" is EXACTLY the same thing the routing layer used to
    auto-proceed -- no inconsistency between pipeline behaviour and report text.

Writing quality rules (apply to every text field produced here):
    1. Confidence language must never overclaim. Always HIGH/MODERATE/LOW,
       never a percentage presented as a calibrated probability.
    2. Never state a definitive legal verdict. Use observation + recommendation
       pattern: "The system identified [X]. Qualified legal review is recommended."

Banned phrases (checked by executive_summary.py on LLM output, also manually
avoided in all templated text produced by this module):
    "is enforceable", "is unenforceable", "is valid", "is invalid",
    "legally binding", "guarantees", "certifies that"
"""

