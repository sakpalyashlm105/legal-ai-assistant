class TestBannedPhraseRetryFallback:
    VERDICT_RESPONSE = (
        "This contract is legally binding and all clauses are valid under "
        "applicable law. The system found no issues."
    )
    # CLEAN_RESPONSE must now satisfy BOTH validators:
    #   (1) no banned verdict phrases
    #   (2) evidence-coverage check: _make_report(with_missing=True) has 9 present
    #       + 1 absent, so the response must explicitly distinguish them.
    CLEAN_RESPONSE = (
        "This NDA contains 9 of 10 standard clause categories. "
        "All 9 present clauses passed evidence verification; "
        "the remaining 1 clause (Indemnification) was absent and not applicable "
        "for this check. "
        "The system identified one high-risk finding (Indemnification). "
        "Qualified legal review is recommended before execution."
    )

    def test_first_attempt_verdict_second_clean_returns_clean(self):
        """First LLM response has banned phrase; second is clean -> use second."""
        report = _make_report(with_missing=True)
        responses = iter([self.VERDICT_RESPONSE, self.CLEAN_RESPONSE])

        with patch("reporting.executive_summary._call_llm", side_effect=responses):
            result = generate_executive_summary(report)

        assert result == self.CLEAN_RESPONSE

    def test_both_attempts_verdict_returns_fallback(self):
        """Both LLM responses have banned phrases -> return deterministic fallback."""
        report = _make_report(with_missing=True, with_high_risk=True)

        with patch(
            "reporting.executive_summary._call_llm",
            return_value=self.VERDICT_RESPONSE,
        ):
            result = generate_executive_summary(report)

        # Result should be the fallback (which has no banned phrases)
        lower = result.lower()
        for phrase in BANNED_PHRASES:
            assert phrase.lower() not in lower, (
                f"Fallback after double-failure contains banned phrase: '{phrase}'"
            )
        assert isinstance(result, str)
        assert len(result) > 10

    def test_banned_phrase_never_silently_published(self):
        """The banned phrase from the mock response must NOT appear in the result."""
        report = _make_report()
        with patch(
            "reporting.executive_summary._call_llm",
            return_value=self.VERDICT_RESPONSE,
        ):
            result = generate_executive_summary(report)

        assert "legally binding" not in result.lower(), (
            "Banned phrase 'legally binding' was silently published in the result"
        )
        assert "is valid" not in result.lower(), (
            "Banned phrase 'is valid' was silently published in the result"
        )


