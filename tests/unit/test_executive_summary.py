def _make_report(with_missing: bool = False, with_high_risk: bool = False) -> LegalDocumentReport:
    from config import CLAUSE_CATEGORIES
    from datetime import datetime

    clause_entries = []
    risk_findings = []
    missing_clauses = []

    for cat in CLAUSE_CATEGORIES:
        is_present = not (with_missing and cat == "Indemnification")
        risk_level = "HIGH" if (with_high_risk and cat == "Indemnification") else "LOW"

        clause_entries.append(ClauseReportEntry(
            clause_category=cat,
            is_present=is_present,
            confidence_label="HIGH",
            confidence_explanation="High confidence: clause verified in source.",
            risk_level=risk_level if is_present else "HIGH",
        ))
        risk_findings.append(RiskFindingEntry(
            clause_category=cat,
            risk_level=risk_level,
            finding_summary=(
                f"The system identified the {cat} clause. "
                "Qualified legal review is recommended."
            ),
            human_review_status="not_required",
        ))
        if not is_present:
            missing_clauses.append(cat)

    return LegalDocumentReport(
        document_name="NDA_test.pdf",
        document_hash="cafebabe1234",
        document_type="NDA",
        classification_confidence_label="HIGH",
        total_pages=5,
        total_clauses_found=len([c for c in clause_entries if c.is_present]),
        total_clauses_missing=len(missing_clauses),
        executive_summary="Placeholder -- will be replaced by generate_executive_summary.",
        clause_entries=clause_entries,
        risk_findings=risk_findings,
        missing_clauses=missing_clauses,
        guardrail_summary=[GuardrailSummaryEntry(
            guardrail_name="input_validator.all_checks",
            passed=True,
            severity="info",
            reason="All checks passed.",
        )],
        limitations_note="Template comparison data was not available for all clauses.",
        disclaimer=(
            "This report was generated by an AI-assisted analysis system. "
            "It does not constitute legal advice."
        ),
        generated_at=datetime(2026, 6, 26, 12, 0, 0),
    )


