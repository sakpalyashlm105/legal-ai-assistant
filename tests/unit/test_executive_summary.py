class TestEvidenceVerificationPhrasing:
    """
    Validates that both the fallback template and the LLM path correctly
    distinguish present-and-verified clauses from absent-and-N/A clauses.

    The specific anti-pattern being guarded: when 6 of 10 clauses are absent
    and 4 are present (all verified), the summary must NOT read as though all
    10 were verified clean.  It must state explicitly that N clauses passed
    verification and M were absent/not-applicable.
    """

    def _make_mixed_report(self) -> LegalDocumentReport:
        """4 present (all evidence_verified=True) + 6 absent — mirrors the real NDA run."""
        from config import CLAUSE_CATEGORIES
        from datetime import datetime

        present_cats = CLAUSE_CATEGORIES[:4]   # first 4 present
        absent_cats  = CLAUSE_CATEGORIES[4:]   # last 6 absent

        clause_entries = []
        risk_findings  = []
        missing_clauses = []

        for cat in present_cats:
            clause_entries.append(ClauseReportEntry(
                clause_category=cat,
                is_present=True,
                confidence_label="HIGH",
                confidence_explanation="Clause found.",
                evidence_verified=True,
                risk_level="LOW",
            ))
            risk_findings.append(RiskFindingEntry(
                clause_category=cat,
                risk_level="LOW",
                finding_summary=f"The system identified the {cat} clause.",
                human_review_status="not_required",
            ))

        for cat in absent_cats:
            clause_entries.append(ClauseReportEntry(
                clause_category=cat,
                is_present=False,
                confidence_label="LOW",
                confidence_explanation="Clause not found.",
                evidence_verified=None,
                risk_level="HIGH",
            ))
            risk_findings.append(RiskFindingEntry(
                clause_category=cat,
                risk_level="HIGH",
                finding_summary=f"The system identified that the {cat} clause is absent.",
                human_review_status="not_required",
            ))
            missing_clauses.append(cat)

        return LegalDocumentReport(
            document_name="NDA_mixed.pdf",
            document_hash="deadbeef1234",
            document_type="NDA",
            classification_confidence_label="HIGH",
            total_pages=4,
            total_clauses_found=len(present_cats),
            total_clauses_missing=len(absent_cats),
            executive_summary="Placeholder.",
            clause_entries=clause_entries,
            risk_findings=risk_findings,
            missing_clauses=missing_clauses,
            guardrail_summary=[GuardrailSummaryEntry(
                guardrail_name="input_validator.all_checks",
                passed=True,
                severity="info",
                reason="All checks passed.",
            )],
            limitations_note="",
            disclaimer="This report was generated by an AI-assisted analysis system.",
            generated_at=datetime(2026, 6, 26, 12, 0, 0),
        )

    # --- Fallback template tests (fully deterministic, no LLM mock needed) ---

    def test_fallback_does_not_say_no_evidence_issues_when_na_exists(self):
        """
        When absent clauses exist, the fallback must NOT produce the misleading
        phrase 'no evidence verification issues' (which implies absent clauses
        were checked and found clean).
        """
        from reporting.report_generator import _fallback_executive_summary

        report = self._make_mixed_report()
        result = _fallback_executive_summary(
            document_type=report.document_type,
            total_found=report.total_clauses_found,
            risk_entries=list(report.risk_findings),
            missing_names=list(report.missing_clauses),
            clause_entries=list(report.clause_entries),
        )
        lower = result.lower()
        assert "no evidence verification issues" not in lower, (
            "Fallback summary used misleading 'no evidence verification issues' "
            "phrasing when 6 absent clauses were N/A for verification."
        )

    def test_fallback_states_present_clauses_passed_explicitly(self):
        """Fallback must say how many present clauses passed evidence verification."""
        from reporting.report_generator import _fallback_executive_summary

        report = self._make_mixed_report()
        result = _fallback_executive_summary(
            document_type=report.document_type,
            total_found=report.total_clauses_found,
            risk_entries=list(report.risk_findings),
            missing_names=list(report.missing_clauses),
            clause_entries=list(report.clause_entries),
        )
        # The fallback should say "4 present clause(s) passed evidence verification"
        assert "4 present" in result and "passed evidence verification" in result, (
            f"Fallback should explicitly state 4 present clauses passed verification. Got:\n{result}"
        )

    def test_fallback_states_absent_clauses_not_applicable(self):
        """Fallback must say absent clauses were not applicable for verification."""
        from reporting.report_generator import _fallback_executive_summary

        report = self._make_mixed_report()
        result = _fallback_executive_summary(
            document_type=report.document_type,
            total_found=report.total_clauses_found,
            risk_entries=list(report.risk_findings),
            missing_names=list(report.missing_clauses),
            clause_entries=list(report.clause_entries),
        )
        lower = result.lower()
        assert "not applicable" in lower, (
            f"Fallback should state absent clauses were not applicable. Got:\n{result}"
        )

    def test_fallback_na_count_is_correct(self):
        """The N/A count in the fallback must match the number of absent clauses."""
        from reporting.report_generator import _fallback_executive_summary

        report = self._make_mixed_report()
        result = _fallback_executive_summary(
            document_type=report.document_type,
            total_found=report.total_clauses_found,
            risk_entries=list(report.risk_findings),
            missing_names=list(report.missing_clauses),
            clause_entries=list(report.clause_entries),
        )
        # 6 absent clauses -> "6 clause(s) were absent"
        assert "6" in result, (
            f"Fallback should mention 6 (the absent count). Got:\n{result}"
        )

    # --- LLM path: mock a clean response using the correct phrasing pattern ---

    def test_llm_correct_phrasing_is_accepted(self):
        """
        A response that correctly distinguishes verified-from-N/A should pass
        _check_output and be returned unchanged.
        """
        correct_response = (
            "The NDA contains 4 of 10 standard clause categories. "
            "All 4 present clauses passed evidence verification; "
            "the remaining 6 clauses were absent and not applicable for this check. "
            "The system identified 6 high-risk findings for absent clause categories. "
            "Qualified legal review is recommended before execution."
        )
        report = self._make_mixed_report()
        with patch("reporting.executive_summary._call_llm", return_value=correct_response):
            result = generate_executive_summary(report)
        assert result == correct_response

    def test_llm_ambiguous_phrasing_triggers_retry_or_fallback(self):
        """
        A response that uses the ambiguous 'no evidence verification issues detected'
        phrasing is NOT caught by _check_output (it's not a banned verdict phrase),
        but after our prompt change the LLM should no longer produce it. This test
        documents the expected fallback behavior if both LLM attempts fail a separate
        check, and confirms the fallback uses correct phrasing instead.
        """
        # Simulate both LLM attempts failing (API error) -> fallback used
        report = self._make_mixed_report()
        with patch("reporting.executive_summary._call_llm", side_effect=Exception("fail")):
            result = generate_executive_summary(report)
        # The fallback (not the misleading LLM response) should be returned
        assert "not applicable" in result.lower(), (
            "When LLM fails, fallback must use correct N/A phrasing."
        )


