"""
tests/unit/test_executive_summary.py
--------------------------------------
Unit tests for reporting/executive_summary.py.

The LLM call is mocked in all tests so no real API calls are made.
Tests use the same mocking pattern established in the existing
unit test suite (patch at the module import boundary).

What is tested:
    1. Successful LLM call returns the generated text.
    2. API error (any Exception) triggers fallback -- never raises.
    3. A response containing a banned verdict phrase is caught, one retry
       is attempted, and if retry also fails the check, fallback is returned.
    4. The fallback summary is itself rule-compliant (no banned phrases,
       no percentage probabilities, ends with a review recommendation).
    5. The post-generation checker (_check_output) correctly identifies
       each banned phrase and accepts clean text.
"""

