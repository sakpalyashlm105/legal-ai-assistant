# Correct phrasing that should pass the validator in any scenario
_CORRECT_PHRASING = (
    "The NDA contains 4 of 10 standard clause categories. "
    "All 4 present clauses passed evidence verification; "
    "the remaining 6 clauses were absent and not applicable for this check. "
    "The system identified 6 high-risk findings. "
    "Qualified legal review is recommended."
)

# Deliberately "bad-sounding" blanket phrasing — only acceptable when na_count == 0
_BLANKET_BAD_PHRASING = (
    "The NDA contains 10 of 10 standard clause categories. "
    "No evidence verification issues were detected. "
    "All checks passed. "
    "Qualified legal review is recommended."
)


class TestCheckEvidenceCoveragePhrasing:
    """
    Unit tests for check_evidence_coverage_phrasing().

    Mirrors the structure of TestCheckOutput for the verdict-phrase scanner.
    """

    # --- na_count == 0: unconditional pass ---

    def test_na_zero_any_phrasing_passes(self):
        """When na_count == 0, even blanket phrasing is acceptable (nothing is absent)."""
        assert check_evidence_coverage_phrasing(_BLANKET_BAD_PHRASING,
                                                verified_count=10, na_count=0) is True

    def test_na_zero_verified_count_irrelevant(self):
        """verified_count value is ignored when na_count == 0."""
        assert check_evidence_coverage_phrasing("Anything.", verified_count=0, na_count=0) is True

    # --- na_count > 0: ambiguous phrases must be caught ---

    @pytest.mark.parametrize("label,text", [
        ("all_clauses_verified",
         "The NDA analysis found that all clauses were verified against the source."),
        ("no_evidence_issues_blanket",
         "No evidence verification issues were detected in this document."),
        ("no_evidence_issues_blanket_found",
         "No evidence concerns were found."),
        ("no_evidence_concerns_blanket",
         "There were no significant evidence concerns identified."),
        ("evidence_verification_blanket_completion",
         "Evidence verification was completed successfully for the document."),
        ("all_evidence_checks_passed",
         "All evidence checks passed for this agreement."),
        ("everything_verified",
         "Everything was verified against the PDF source text."),
        ("evidence_check_complete",
         "The evidence check was complete and thorough."),
    ])
    def test_ambiguous_phrase_rejected_when_na_exists(self, label, text):
        """Each ambiguous pattern is rejected when na_count > 0."""
        result = check_evidence_coverage_phrasing(text, verified_count=4, na_count=6)
        assert result is False, (
            f"Pattern '{label}' should be rejected when na_count=6. "
            f"Text: {text!r}"
        )

    def test_ambiguous_phrase_case_insensitive(self):
        """Ambiguous phrase detection is case-insensitive."""
        upper = "NO EVIDENCE VERIFICATION ISSUES WERE DETECTED."
        assert check_evidence_coverage_phrasing(upper, verified_count=4, na_count=6) is False

    # --- na_count > 0: missing distinction signals must be caught ---

    def test_missing_absent_signal_rejected(self):
        """
        Summary that mentions present-clause verification but never mentions
        absent clauses being N/A should be rejected.
        """
        only_present = (
            "The system analyzed the NDA. "
            "All 4 present clauses passed evidence verification. "
            "High-risk findings were identified. "
            "Qualified legal review is recommended."
        )
        assert check_evidence_coverage_phrasing(only_present, verified_count=4, na_count=6) is False

    def test_missing_present_signal_rejected(self):
        """
        Summary that mentions absent clauses being N/A but never mentions
        present-clause verification should be rejected.
        """
        only_absent = (
            "The NDA is missing 6 of 10 standard clauses, which are not applicable. "
            "High-risk findings were identified. "
            "Qualified legal review is recommended."
        )
        assert check_evidence_coverage_phrasing(only_absent, verified_count=4, na_count=6) is False

    def test_no_signals_at_all_rejected(self):
        """Summary that mentions neither population should be rejected when na_count > 0."""
        no_signals = (
            "This NDA has 4 of 10 clause categories present. "
            "Several high-risk findings were identified. "
            "Qualified legal review is recommended."
        )
        assert check_evidence_coverage_phrasing(no_signals, verified_count=4, na_count=6) is False

    # --- na_count > 0: correct phrasing passes ---

    def test_correct_phrasing_passes(self):
        """The phrasing pattern confirmed in the real LLM run must pass."""
        assert check_evidence_coverage_phrasing(
            _CORRECT_PHRASING, verified_count=4, na_count=6
        ) is True

    def test_correct_phrasing_real_llm_text(self):
        """The actual text produced by the real LLM run (previous task) must pass."""
        real_llm_text = (
            "The analysis of the Non-Disclosure Agreement (NDA) revealed that "
            "4 out of 10 standard clause categories were present. "
            "High-risk findings were identified in the areas of Confidentiality / "
            "Non-Disclosure, Termination for Convenience, Termination for Cause, "
            "Governing Law / Jurisdiction, Indemnification, Limitation of Liability, "
            "Non-Compete / Non-Solicitation, and Assignment. "
            "All 4 present clauses passed evidence verification, while 6 absent "
            "clauses were not applicable for verification. "
            "Given the high-risk findings and the missing clauses, it is recommended "
            "that a qualified legal professional review the document."
        )
        assert check_evidence_coverage_phrasing(
            real_llm_text, verified_count=4, na_count=6
        ) is True

    def test_all_present_no_absent_with_correct_text(self):
        """When all 10 clauses are present (na_count=0), validator passes unconditionally."""
        full_coverage_text = (
            "This NDA contains all 10 standard clause categories. "
            "All clauses were verified and no evidence issues were detected. "
            "Qualified legal review is recommended."
        )
        # Even though this text has "no evidence issues were detected"
        # (which would be flagged if na_count > 0), na_count=0 means there's
        # nothing absent to mis-describe, so this passes.
        assert check_evidence_coverage_phrasing(
            full_coverage_text, verified_count=10, na_count=0
        ) is True


